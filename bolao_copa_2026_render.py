def calcular_pontos_placar(
    gols_a_real,
    gols_b_real,
    gols_a_palpite,
    gols_b_palpite
):


    # ----------------------------
    # ACERTO EXATO
    # ----------------------------

    if (
        gols_a_real == gols_a_palpite
        and
        gols_b_real == gols_b_palpite
    ):

        return 12, "Placar exato"



    # ----------------------------
    # IDENTIFICAR RESULTADOS
    # ----------------------------

    def resultado(a,b):

        if a > b:

            return "A"

        elif b > a:

            return "B"

        else:

            return "E"



    resultado_real = resultado(
        gols_a_real,
        gols_b_real
    )


    resultado_palpite = resultado(
        gols_a_palpite,
        gols_b_palpite
    )



    acertou_resultado = (
        resultado_real == resultado_palpite
    )


    acertou_algum_placar = (
        gols_a_real == gols_a_palpite
        or
        gols_b_real == gols_b_palpite
    )



    # ----------------------------
    # VENCEDOR + UM PLACAR
    # ----------------------------

    if (
        acertou_resultado
        and
        acertou_algum_placar
    ):

        return 8, "Resultado + um placar"



    # ----------------------------
    # SOMENTE RESULTADO
    # ----------------------------

    if acertou_resultado:

        return 5, "Resultado correto"



    # ----------------------------
    # SOMENTE UM PLACAR
    # INDEPENDENTE DO RESULTADO
    # ----------------------------

    if acertou_algum_placar:

        return 2, "Um placar correto"



    # ----------------------------
    # ERRO COMPLETO
    # ----------------------------

    return 0, "Erro completo"



