def calcular_pontos(real_a, real_b, palpite_a, palpite_b):


    try:

        real_a = int(real_a)
        real_b = int(real_b)

        palpite_a = int(palpite_a)
        palpite_b = int(palpite_b)


    except:

        return 0, "Sem placar"



    # 12 pontos — placar exato

    if real_a == palpite_a and real_b == palpite_b:

        return 12, "Placar exato"



    resultado_real = (

        real_a > real_b

    ) - (

        real_a < real_b

    )


    resultado_palpite = (

        palpite_a > palpite_b

    ) - (

        palpite_a < palpite_b

    )



    # 8 pontos

    if (

        resultado_real == resultado_palpite

        and

        (

            real_a == palpite_a

            or

            real_b == palpite_b

        )

    ):

        return 8, "Resultado + um placar"



    # 5 pontos

    if resultado_real == resultado_palpite:

        return 5, "Resultado correto"



    # 2 pontos

    if (

        real_a == palpite_a

        or

        real_b == palpite_b

    ):

        return 2, "Um placar correto"



    return 0, "Erro completo"



